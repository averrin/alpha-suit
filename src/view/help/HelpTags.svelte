<h1>Alpha Tree tags</h1>

<ul>
   <li>Tags are just a string attached to a document. They can contain spaces and any symbols you want.</li>
   <li>The top filterbar searches by exact match for tags and by regex match for names.</li>
   <li>You can click on a tag in a tree item to search by this tag</li>
   <li>
      Clicking on a "cross" button removes tag, a single click on a tag in a editor removes it but keeps it's text in a
      input button.
   </li>
   <li>
      Right click on a tag opens Tag Settings dialog to assign icon and color. If you are using Director, you can enable
      using tags from the Director by the setting.
   </li>
   <li>
      If you check "favorite" toggle, tag will be present in the "quick tags" bar. It's useful for frequent filters.
   </li>
</ul>
